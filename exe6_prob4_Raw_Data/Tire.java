package exe6_prob4_Raw_Data;

public class Tire {
    private int tireAge;
    private double tirePressure;

    public Tire(int tireAge, double tirePressure) {
        this.tireAge = tireAge;
        this.tirePressure = tirePressure;
    }

    public int getTireAge() {
        return tireAge;
    }


    public double getTirePressure() {
        return tirePressure;
    }
}

